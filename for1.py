# Ci sono tanti modi di fare il for in Python
# Vediamo il primo modo, detto C-style

for i in range(1, 4):     # Range è una funzione Python che prende un numero # START e GAP è facoltativo # STOP è obbligatorio
    print(i)

# Range([START], STOP, [GAP])
for i in range(8, 1, -2):     # Range è una funzione Python che prende un numero
    print(i)